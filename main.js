// Digital Clock.
const showTime = () => {
    let date = new Date();

    let year = date.getFullYear();

    let month = date.toLocaleString('en-us', { month: 'long' });

    let day = date.getDate();

    let hour = date.getHours();

    let minute = date.getMinutes();

    // AM or PM. AM is default.
    let period = "AM";

    if (hour == 0) {
        hour = 12;
    }

    if (hour > 12) {
        hour = hour - 12;
        period = "PM";
    }

    hour = (hour < 10) ? "" + hour : hour;
    minute = (minute < 10) ? "0" + minute : minute;

    let time = hour + ":" + minute;

    document.getElementById('time').innerText = time;
    document.getElementById('period').innerText = period;
    document.getElementById('date').innerText = `${month} ${day}, ${year}`;

    setTimeout(showTime, 1000);
}

// Create divs with mock data when website first loads.
const onLoad = () => {

    showTime();

    // r/javascript scrollable newsfeed.
    redditNewsFeed.forEach(post => {
        let content = document.createElement('p');
        content.className = 'content';
        content.textContent = post.content;

        let postedBy = document.createElement('p');
        postedBy.className = 'postedBy';
        postedBy.textContent = 'Posted by: ';
        let user = document.createElement('span');
        user.className = 'user'
        user.textContent = post.user;
        postedBy.append(user);

        let score = document.createElement('p');
        score.className = 'redditScore';
        score.textContent = `Reddit Score: ${post.score}`;

        let linkToComments = document.createElement('p');
        linkToComments.className = 'linkToComments';
        linkToComments.textContent = 'Link to Comments';


        let newDiv = document.createElement('div');
        newDiv.className = 'post'
        newDiv.append(content);
        newDiv.append(postedBy);
        newDiv.append(score);
        newDiv.append(linkToComments);

        let redditPosts = document.getElementById('redditPosts')
        redditPosts.append(newDiv);
    });

    // JavaScript from users own history.
    javascriptHistory.forEach(post => {
        let postsContent = document.createElement('p');
        postsContent.className = 'postsContent'

        let postBody = document.createElement('span');
        postBody.className = 'postBody';
        postBody.textContent = post.content;
        postsContent.append(postBody);

        let postTime = document.createElement('span');
        postTime.className = 'postTime';
        postTime.textContent = post.timeAgo;
        postsContent.append(postTime);

        let historyContent = document.getElementById('historyContent');
        historyContent.append(postsContent)

    });

    // Scrollable github issues.
    githubIssues.forEach(issue => {
        let repo = document.createElement('p');
        repo.className = 'repo';
        repo.innerText = issue.repo;

        let issueNumber = document.createElement('p');
        issueNumber.className = 'issueNumber';
        issueNumber.innerText = issue.issueNumber;

        let content = document.createElement('p');
        content.className = 'content';
        content.innerText = issue.content;

        let issueDiv = document.createElement('div');
        issueDiv.className = 'issue';
        issueDiv.append(repo);
        issueDiv.append(issueNumber);
        issueDiv.append(content);

        let githubIssuesDiv = document.getElementById('issues');
        githubIssuesDiv.append(issueDiv);
    })
}